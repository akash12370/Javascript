
//var - can be re-declared and update
var a =10;
console.log("Before - Var variable:",a);
var a = 100;
console.log("After - var variable:",a);

//let - cannot be re-declared but can be updated
let b = 10;
console.log("Let variable:",b);
b = 100;
console.log("Let variable:",b);

//const - cannot be re-declared or updated
const pi = 3.14;
console.log("Const variable:",pi);
